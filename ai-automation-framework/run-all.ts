#!/usr/bin/env node
/**
 * run-all.ts вЂ” Single command to run automation + launch presentation UI
 * Usage: npm run run-all -- --url="https://your-site.com"
 */
import { execSync, spawn } from 'child_process';
import path from 'path';

const args = process.argv.slice(2);
const urlArg = args.find(a => a.startsWith('--url='));

if (!urlArg) {
  console.error('\nвќЊ  Please provide --url:\n   npm run run-all -- --url="https://your-site.com"\n');
  process.exit(1);
}

const ROOT = path.resolve(__dirname);
const RENDERER_DIR = path.join(ROOT, 'presentation-renderer');

console.log('\nв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ');
console.log(' рџ¤– AI Automation Framework вЂ” Full Pipeline');
console.log('в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n');

// Step 1: Run the full automation pipeline
console.log(`в–¶ STEP 1: Running automation for ${urlArg.replace('--url=', '')}`);
console.log('в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ\n');

try {
  execSync(`npx ts-node index.ts ${urlArg}`, {
    cwd: ROOT,
    stdio: 'inherit'
  });
} catch (e) {
  console.error('\nвќЊ  Automation pipeline failed. Check the error above.\n');
  process.exit(1);
}

// Step 2: Launch presentation renderer
console.log('\nв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ');
console.log('в–¶ STEP 2: Launching Presentation Renderer UI');
console.log('в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ\n');

const server = spawn('node', ['server.cjs'], {
  cwd: RENDERER_DIR,
  stdio: 'inherit',
  detached: false
});

server.on('error', (err) => {
  console.error('вќЊ  Failed to start presentation server:', err.message);
});

process.on('SIGINT', () => {
  console.log('\n\nрџ‘‹ Shutting down presentation server...\n');
  server.kill();
  process.exit(0);
});
